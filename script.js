function update_widget() {
    const social = socials[currentSocialIndex];

    card.style.opacity = '0';

    window.setTimeout(() => {
        if(useAltIcon && social.name == 'Twitter') {
            iconContainer.innerHTML = social.iconAlt;
        } else {
            iconContainer.innerHTML = social.icon;
        }
        textContainer.textContent = social.username;
        currentSocialIndex = (currentSocialIndex + 1) % socials.length;
        card.style.opacity = '1';
    }, 500);
}
